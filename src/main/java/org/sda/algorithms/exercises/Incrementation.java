package org.sda.algorithms.exercises;

public class Incrementation {

    public static void main(String[] args){
        int i = 0;

        i++;
        System.out.println(i); // 1
        System.out.println(i++); // 1
        System.out.println(++i); // 3
        System.out.println(i--); // 3
        System.out.println(--i); // 1
    }
}
